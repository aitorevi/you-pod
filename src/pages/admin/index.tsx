import Head from 'next/head'
import {Inter} from 'next/font/google'
import Nav from '../../../components/navbar'
import FooterSimple from '../../../components/footer'
import React from "react";
import {Button, Heading} from '@chakra-ui/react'
import {Box, Container, Stack, useColorModeValue} from "@chakra-ui/react";

import {useEffect, useState} from "react";
import {AdminPanel} from "../../../components/adminPanel";
import {CreateModal} from "../../../components/createModal";
import {collection, deleteDoc, doc, getDocs, orderBy, query, setDoc, updateDoc} from "firebase/firestore";
import {db} from "../../../firebase/firebase";

const inter = Inter({subsets: ['latin']})
export default function Podcast() {
    const [podcastList, setPodcastList] = useState(null);
    const ReadPodcastInDB = async () => {
        const result = await getDocs(query(collection(db, "podcast"), orderBy("createAt", "desc")));
        return result;
    }
    const DeletePodcastInDB = async (id: string) => {
        {
            await deleteDoc(doc(db, "podcast", id));
            await readPodcast();
        }
    }
    const deletePodcast = async (id: string) => {
        await DeletePodcastInDB(id)
    }
    // const UpdatePodcastInDB = async (id: string, title: string, description: string, url: string) => {
    //     {
    //         await updateDoc(doc(db, "podcast", id), {title, description, url});
    //         await readPodcast();
    //     }
    // }
    // const updatePodcast = async (id: string, title: string, description: string, url: string) => {
    //     await UpdatePodcastInDB(id, title, description, url)
    // }
    useEffect(() => {
        readPodcast().then(r => r);
    }, [])
    const readPodcast = async () => {
        const podcasts = await ReadPodcastInDB();
        // @ts-ignore
        setPodcastList(podcasts.docs);
    }
    return (
        <>
            <Head>
                <title>Podcast</title>
                <meta name="description" content="Generated by create next app"/>
                <meta name="viewport" content="width=device-width, initial-scale=1"/>
                <link rel="icon" href="/favicon.ico"/>
            </Head>
            <Nav/>
            <Container maxW='5xl'>
                <Box
                    bg={useColorModeValue('white', 'gray.800')}
                    minH={'100vh'}
                    py={2}
                    px={2}
                >
                    <Stack>
                        <Container
                            as={Stack}
                            maxW={'6xl'}
                            marginTop='0.5rem'
                            direction={{base: 'column', md: 'row'}}
                            spacing={{base: 4, md: 10}}
                            justify={{base: 'center', md: 'center'}}
                            bg={useColorModeValue('gray.200', 'gray.800')}
                            color={'gray.700'}
                            borderRadius={6}>
                            <Heading
                                size='xl'>
                                Panel de control
                            </Heading>
                        </Container>
                        <Container
                            as={Stack}
                            maxW={'6xl'}
                            marginTop='0.5rem'
                            paddingY={2}
                            direction={{base: 'column', md: 'row'}}
                            spacing={{base: 4, md: 10}}
                            justify={{base: 'space-between', md: 'space-between'}}
                            align={{base: 'start', md: 'start'}}
                            bg={useColorModeValue('gray.50', 'gray.800')}
                            borderRadius={6}>
                            <CreateModal
                                readPodcast={readPodcast}/>
                        </Container>
                        {
                            podcastList && podcastList.map(podcast => {
                                return (
                                    <AdminPanel readPodcast={readPodcast}
                                                deletePodcast={deletePodcast}
                                                key={podcast.id}
                                                id={podcast.id}
                                                url={podcast.data().url}
                                                title={podcast.data().title}
                                                description={podcast.data().description}/>
                                )
                            })
                        }
                    </Stack>
                </Box>
            </Container>
            <footer>
                <FooterSimple/>
            </footer>
        </>
    )
}